<template lang="pug">
  no-ssr
    a.button(v-cloak
             v-if="account"
             href="/pdf/Vue-Essentials-Cheat-Sheet.pdf"
             :class="buttonClass"
             download) Download it Free

    button.button(:class="buttonClass" v-else v-cloak @click="openLogin") Download it Free
</template>

<script>
import { mapState } from 'vuex'

export default {
  name: 'downloadButton',
  props: ['buttonClass', 'location'],
  computed: {
    ...mapState({
      account: result => result.account.account
    })
  },
  methods: {
    openLogin () {
      this.$modal.show('login-form', {
        newAccount: true,
        headerTitle: 'Please sign up to download free',
        redirect: '/download',
        location: this.location
      })
    }
  }
}
</script>
